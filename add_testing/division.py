def division(a, b):
    """
    This function divides one values to other values.

    Args:
        a: an integer no
        b: an integer no
    
    Returns:
        divided value
    """
    return a / b

def floor_division(a, b):
    """
    This function floor divides one values to other values.

    Args:
        a: an integer no
        b: an integer no
    
    Returns:
        floor division value
    """
    return a // b
